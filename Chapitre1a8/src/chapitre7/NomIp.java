package chapitre7;

public class NomIp {

	private String nom;
	private String adresse;
	
	
	public NomIp(String nomIp,String adresse) {
		this.nom=nomIp;
		this.adresse=adresse;
	}
	public String getNom() {
		return nom;
	}
	public String getAdresse() {
		return adresse;
	}
	
}
